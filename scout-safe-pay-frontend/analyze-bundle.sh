#!/bin/bash
echo "📊 BUNDLE SIZE ANALYSIS"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

echo "1. Total Build Size:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
du -sh .next 2>/dev/null
echo ""

echo "2. Static Assets:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
du -sh .next/static 2>/dev/null
echo ""

echo "3. Largest Files:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
find .next/static -type f -size +100k 2>/dev/null | xargs du -h | sort -hr | head -10
echo ""

echo "4. JavaScript Chunks:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
find .next/static/chunks -name "*.js" 2>/dev/null | wc -l | xargs echo "Total chunks:"
find .next/static/chunks -name "*.js" -size +200k 2>/dev/null | xargs du -h | sort -hr | head -5
echo ""

echo "5. Pages Size:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
if [ -d ".next/app" ]; then
    du -sh .next/app 2>/dev/null
fi
echo ""

echo "6. Public Assets:"
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
du -sh public 2>/dev/null
echo ""

